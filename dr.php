        <h1 id='awareness-title'>Raising Awareness on Diabetic Retinopathy: Protecting Your Vision</h1>
        <p class='awareness-para'>Diabetic Retinopathy is a serious eye condition that affects people with diabetes. It is caused by damage to the blood vessels of the light-sensitive tissue at the back of the eye (retina). <b class='awareness-consult'>If you have diabetes, regular eye check-ups with an eye care professional are crucial to detect and manage Diabetic Retinopathy at an early stage.</b></p>

        <h2 id='awareness-subtitle'>Understanding Diabetic Retinopathy</h2>

        <p class='awareness-para'>Diabetic Retinopathy is a leading cause of blindness in adults. It occurs when high levels of blood sugar lead to damage in the blood vessels of the retina. There are four main stages:</p>

        <ol class='awareness-para'>
            <li>Stage 1: Mild Nonproliferative Diabetic Retinopathy</li>
            <li>Stage 2: Moderate Nonproliferative Diabetic Retinopathy</li>
            <li>Stage 3: Severe Nonproliferative Diabetic Retinopathy</li>
            <li>Stage 4: Proliferative Diabetic Retinopathy</li>
        </ol>

        <p class='awareness-para'>Nonproliferative Diabetic Retinopathy (NPDR) is the early stage, progressing from mild to severe. Proliferative Diabetic Retinopathy (PDR) is the advanced stage characterized by the growth of abnormal blood vessels on the retina, which can lead to severe complications.</p>

        <div class='awareness-image'>
            <img class='awareness-img' src='assets\disease image\Diabetic Retinopathy.jpg' alt='Diabetic Retinopathy Awareness'>
        </div>

        <h2 id='awareness-subtitle'>Risk Factors and Prevention</h2>

        <p class='awareness-para'>People with diabetes are at a higher risk of developing Diabetic Retinopathy. It's essential to manage blood sugar levels, blood pressure, and cholesterol to reduce the risk. Regular eye examinations and a healthy lifestyle play a crucial role in prevention.</p>

        <h2 id='awareness-subtitle'>Signs and Symptoms</h2>

        <p class='awareness-para'>Early stages of Diabetic Retinopathy may not show noticeable symptoms. As the condition progresses, symptoms may include:</p>

        <ul class='awareness-para'>
            <li>Blurred or fluctuating vision</li>
            <li>Floaters or dark spots in vision</li>
            <li>Difficulty seeing well at night</li>
            <li>Loss of central vision</li>
        </ul>

        <h2 id='awareness-subtitle'>Types of Diabetic Retinopathy</h2>

        <p class='awareness-para'>Beyond the main stages, Diabetic Retinopathy can be categorized into:</p>

        <ul class='awareness-para'>
            <li>Macular Edema: Swelling in the macula, the part of the retina responsible for central vision.</li>
            <li>Diabetic Maculopathy: Affecting the macula and causing vision loss.</li>
        </ul>

        <h2 id='awareness-subtitle'>Detection and Diagnosis</h2>

        <p class='awareness-para'>Early detection is vital. Regular eye exams, including dilated eye tests, help in identifying signs of Diabetic Retinopathy. Optical Coherence Tomography (OCT) and Fluorescein Angiography are diagnostic tools that provide detailed images of the retina.</p>

        <h2 id='awareness-subtitle'>Why Awareness Matters</h2>

        <p class='awareness-para'>Raising awareness about Diabetic Retinopathy is essential for early detection and management. It empowers individuals with diabetes to prioritize eye care, leading to better outcomes and preserving vision. Regular screenings and timely intervention can make a significant difference in preventing vision loss.</p>

        <h2 id='awareness-subtitle'>Preventive Measures</h2>

        <p class='awareness-para'>Preventing Diabetic Retinopathy involves:</p>

        <ul class='awareness-para'>
            <li>Maintaining optimal blood sugar levels through a balanced diet and medications.</li>
            <li>Controlling blood pressure and cholesterol levels.</li>
            <li>Regular exercise to promote overall health.</li>
            <li>Quitting smoking, as it contributes to diabetic complications.</li>
        </ul>

        <h2 id='awareness-subtitle'>Conclusion</h2>

        <p class='awareness-para'>Diabetic Retinopathy requires attention and proactive management. By spreading awareness, we can encourage those with diabetes to prioritize eye health and undergo regular eye check-ups. Remember, early detection is key to maintaining good vision. If you have diabetes, consult with your eye care professional for personalized advice.</p>

